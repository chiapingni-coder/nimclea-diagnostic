#!/usr/bin/env node

import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import readline from "node:readline/promises";
import { stdin as input, stdout as output } from "node:process";

function normalizeValue(value = "") {
  return String(value || "").trim();
}

function toSlug(value = "") {
  return normalizeValue(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .replace(/-{2,}/g, "-");
}

function toChangeId(value = "") {
  return normalizeValue(value)
    .toUpperCase()
    .replace(/[^A-Z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "")
    .replace(/_{2,}/g, "_");
}

function splitList(value = "") {
  return normalizeValue(value)
    .split(/\r?\n|,/)
    .map((item) => item.trim())
    .filter(Boolean);
}

function formatList(items = []) {
  if (!Array.isArray(items) || items.length === 0) {
    return "- (none provided)";
  }

  return items.map((item) => `- ${item}`).join("\n");
}

const rl = readline.createInterface({ input, output });

try {
  const changeIdInput = await rl.question("changeId: ");
  const shortTitleInput = await rl.question("shortTitle: ");
  const changeTypeInput = await rl.question("changeType: ");
  const protectedBehaviorInput = await rl.question("protectedBehavior: ");
  const touchedFilesInput = await rl.question("touchedFiles (comma or newline separated): ");
  const validationCommandsInput = await rl.question(
    "validationCommands (comma or newline separated): ",
  );

  const changeId = toChangeId(changeIdInput);
  const shortTitle = normalizeValue(shortTitleInput);
  const changeType = normalizeValue(changeTypeInput);
  const protectedBehavior = normalizeValue(protectedBehaviorInput);
  const touchedFiles = splitList(touchedFilesInput);
  const validationCommands = splitList(validationCommandsInput);
  const slug = toSlug(shortTitle || changeId || "protection-pack");

  if (!changeId) {
    throw new Error("changeId is required");
  }

  if (!shortTitle) {
    throw new Error("shortTitle is required");
  }

  const fileName = `NIMCLEA_${changeId}_${slug}_PROTECTION_PACK_V0_1.md`;
  const docsDir = path.resolve("docs");
  const outputPath = path.join(docsDir, fileName);

  await mkdir(docsDir, { recursive: true });

  if (await writeIfMissing(outputPath, buildMarkdown({
    changeId,
    shortTitle,
    changeType,
    protectedBehavior,
    touchedFiles,
    validationCommands,
  }))) {
    console.log(`Created: docs/${fileName}`);
    console.log(`git diff --check -- docs/${fileName}`);
    console.log("git status --short");
  }
} catch (error) {
  console.error(error?.message || error);
  process.exitCode = 1;
} finally {
  rl.close();
}

async function writeIfMissing(filePath, contents) {
  try {
    await writeFile(filePath, contents, { flag: "wx" });
    return true;
  } catch (error) {
    if (error?.code === "EEXIST") {
      throw new Error(`Protection pack already exists: ${filePath}`);
    }

    throw error;
  }
}

function buildMarkdown({
  changeId,
  shortTitle,
  changeType,
  protectedBehavior,
  touchedFiles,
  validationCommands,
}) {
  return `# Nimclea Protection Pack v0.1

## Purpose

Record the completed Nimclea core change and the behavior it protects.

## Protected behavior

${protectedBehavior || "(not provided)"}

## Touched files

${formatList(touchedFiles)}

## Validation commands

${formatList(validationCommands)}

## Required guard/checklist

- Confirm the protected behavior remains unchanged.
- Confirm touched files match the completed change.
- Confirm validation commands were run and passed.
- Confirm the release gate status before treating the change as complete.
- Confirm manual smoke results are recorded when applicable.

## Release gate status

Not run yet.

## Manual smoke result

Not run yet.

## Notes

- Change ID: ${changeId}
- Short title: ${shortTitle}
- Change type: ${changeType || "(not provided)"}
- This file was generated by \`scripts/create-protection-pack.mjs\`.
- Do not commit, push, run migrations, or edit the release gate automatically from this helper.
`;
}
